// set test for Engineer js and its corresponding functions set
const Engineer = require("../lib/Engineer");

describe("Engineer", () => {
    it("should return GitHub profile set by constructor", () => {
        const e = new Engineer("Chewy", 2, "test@test.com", "GitHub");
        expect(e.gitHub).toEqual("GitHub");
    });
    
    it("should return 'Engineer' set by the getRole function", () => {
        const testEngRole = "Engineer";
        const e = new Engineer("Chewy", 2, "test@test.com", "GitHub");
        expect(e.getRole()).toEqual(testEngRole);
    });
    
    it("should return GitHub username set by the getGitHub function", () => {
        const e = new Engineer("Chewy", 2, "test@test.com", "GitHub");
        expect(e.getGitHub()).toEqual("GitHub");
    });
});